#include "../../includes/minishell.h"
#include "../../libft/libft.h"

int	ft_strequ(char *s1, char *s2)
{
	if (!s1 || !s2)
		return (0);
	if (ft_strcmp(s1, s2))
		return (0);
	else
		return (1);
}
